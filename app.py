def init_db():
    db = get_db()
    db.executescript('''
    CREATE TABLE IF NOT EXISTS employees (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT, post TEXT, dept TEXT, phone TEXT, email TEXT, exp INTEGER
    );
    CREATE TABLE IF NOT EXISTS departments (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT, head TEXT, staff INTEGER, phone TEXT, address TEXT, status TEXT
    );
    CREATE TABLE IF NOT EXISTS clients (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT, born INTEGER, cat TEXT, start TEXT, end TEXT, insp TEXT, status TEXT
    );
    CREATE TABLE IF NOT EXISTS events (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        title TEXT, date TEXT, type TEXT, org TEXT, participants INTEGER, status TEXT
    );
    CREATE TABLE IF NOT EXISTS partners (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT, type TEXT, contact TEXT, phone TEXT, email TEXT, area TEXT, since TEXT, status TEXT
    );
    CREATE TABLE IF NOT EXISTS normdocs (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        title TEXT, num TEXT, date TEXT, type TEXT, org TEXT, status TEXT
    );
    CREATE TABLE IF NOT EXISTS appeals (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT, date TEXT, type TEXT, desc TEXT, insp TEXT, status TEXT
    );
    CREATE TABLE IF NOT EXISTS mto (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT, cat TEXT, qty INTEGER, unit TEXT, condition TEXT, location TEXT, note TEXT
    );
    ''')
    db.commit()
    if db.execute('SELECT COUNT(*) FROM employees').fetchone()[0] == 0:
        seed(db)
    db.close()

