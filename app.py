TABLES = {
    'employees':   ['name','post','dept','phone','email','exp'],
    'departments': ['name','head','staff','phone','address','status'],
    'clients':     ['name','born','cat','start','end','insp','status'],
    'events':      ['title','date','type','org','participants','status'],
    'partners':    ['name','type','contact','phone','email','area','since','status'],
    'normdocs':    ['title','num','date','type','org','status'],
    'appeals':     ['name','date','type','desc','insp','status'],
    'mto':         ['name','cat','qty','unit','condition','location','note'],
}

@app.route('/api/<table>')
@login_required
def api_list(table):
    if table not in TABLES: return jsonify({'error':'Not found'}), 404
    db = get_db()
    rows = db.execute(f'SELECT * FROM {table} ORDER BY id').fetchall()
    db.close()
    return jsonify([dict(r) for r in rows])

@app.route('/api/<table>', methods=['POST'])
@login_required
def api_create(table):
    if table not in TABLES: return jsonify({'error':'Not found'}), 404
    data = request.json
    cols = TABLES[table]
    vals = [data.get(c,'') for c in cols]
    db = get_db()
    cur = db.execute(f'INSERT INTO {table}({",".join(cols)}) VALUES({",".join(["?"]*len(cols))})', vals)
    db.commit()
    row = db.execute(f'SELECT * FROM {table} WHERE id=?', (cur.lastrowid,)).fetchone()
    db.close()
    return jsonify(dict(row)), 201

@app.route('/api/<table>/<int:rid>', methods=['PUT'])
@login_required
def api_update(table, rid):
    if table not in TABLES: return jsonify({'error':'Not found'}), 404
    data = request.json
    cols = TABLES[table]
    vals = [data.get(c,'') for c in cols] + [rid]
    db = get_db()
    db.execute(f'UPDATE {table} SET {",".join([f"{c}=?" for c in cols])} WHERE id=?', vals)
    db.commit()
    row = db.execute(f'SELECT * FROM {table} WHERE id=?', (rid,)).fetchone()
    db.close()
    return jsonify(dict(row))

@app.route('/api/<table>/<int:rid>', methods=['DELETE'])
@login_required
def api_delete(table, rid):
    if table not in TABLES: return jsonify({'error':'Not found'}), 404
    db = get_db()
    db.execute(f'DELETE FROM {table} WHERE id=?', (rid,))
    db.commit()
    db.close()
    return jsonify({'ok': True})

@app.route('/api/stats')
@login_required
def api_stats():
    db = get_db()
    stats = {t: db.execute(f'SELECT COUNT(*) FROM {t}').fetchone()[0] for t in TABLES}
    stats['clients_active'] = db.execute("SELECT COUNT(*) FROM clients WHERE status='Активный'").fetchone()[0]
    db.close()
    return jsonify(stats)

